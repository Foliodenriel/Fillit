#include "../include/fillit.h"

void	turn_letter(char **tab)
{
	char	c;
	int		i;
	int		j;

	c = 'A';
	i = 0;
	while (tab[i] != '\0')
	{
		j = 0;
		while (tab[i][j] != '\0')
		{
			if (tab[i][j] == '#')
				tab[i][j] = c;
			else if (tab[i][j] == '\n' && j == 0)
				c++;
			j++;
		}
		i++;
	}
}
